<p>
Most common submarines have masts to raise above ocean surface, these are periscope, radar mast and ESM mast.
</p>

<p>
Periscope is for visual observation and identification of surface targets. You cannot use periscope to spot submarines or other underwater objects. Please note that in Sea Power you as a player do not have a direct control for 3D view of the periscope, you can just raise and lower it.
</p>

<p>
Radar mast is a radar emitter raised above the ocean surface which can detect ships. Using radar mast broadcasts your location to everyone with a ESM receiver.
</p>

<p>
ESM mast is a passive detection method to surveil all radio transmissions on the surface of the ocean.
</p>

